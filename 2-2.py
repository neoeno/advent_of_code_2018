def id_excepting_n(string, n):
    return string[:n] + string[(n + 1):]

def find_common_chars_in_duplicates_excepting_n_char(ids, n):
    seen = set()
    for id in ids:
        if id_excepting_n(id, n) in seen:
            return id_excepting_n(id, n)
        seen.add(id_excepting_n(id, n))
    return None

def find_common_chars_in_duplicates(ids):
    id_length = len(ids[0])
    for i in range(0, id_length):
        result = find_common_chars_in_duplicates_excepting_n_char(ids, i)
        if result is not None:
            return result

